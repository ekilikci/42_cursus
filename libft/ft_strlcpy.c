unsigned int	ft_strlcpy(char *dest, const char *src, unsigned int size)
{
    unsigned int	src_len = 0;
    unsigned int	i = 0;

    while (src[src_len] != '\0')
        src_len++;

    if (size > 0)
    {
        while (src[i] != '\0' && i < (size - 1))
        {
            dest[i] = src[i];
            i++;
        }
        dest[i] = '\0';
    }

    return src_len;
}

